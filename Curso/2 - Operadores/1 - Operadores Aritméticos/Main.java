public class Main {

    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println(a + b); // Adição - Saída: 12
        System.out.println(a - b); // Subtração - Saída: 8
        System.out.println(a * b); // Multiplicação - Saída: 20
        System.out.println(a / b); // Divisão - Saída: 5
        System.out.println(a % b); // Módulo - Saída: 0
    }

}

/*
    Adição (+)
    Subtração (-)
    Multiplicação (*)
    Divisão (/)
    Módulo (resto da divisão) (%)
*/