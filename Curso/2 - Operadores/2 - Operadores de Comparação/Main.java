public class Main {
    
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a == b); // Igualdade - Saída: false
        System.out.println(a != b); // Diferença - Saída: true
        System.out.println(a > b); // Maior - Saída: false
        System.out.println(a < b); // Menor - Saída: true
        System.out.println(a >= b); // Maior ou Igual - Saída: false
        System.out.println(a <= b); // Menor ou Igual - Saída: true
    }

}

/*
    Igual a (==)
    Diferente de (!=)
    Maior que (>)
    Menor que (<)
    Maior ou igual a (>=)
    Menor ou igual a (<=)
*/