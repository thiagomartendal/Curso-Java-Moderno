public class Main {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = true;

        System.out.println(a < b && c); // E lógico - Saída: true
        System.out.println(a > b || c); // OU lógico - Saída: true
        System.out.println(!c); // NÃO lógico - Saída: false
    }

}

/*
    E lógico (&&)
    OU lógico (||)
    NÃO lógico (!)
*/