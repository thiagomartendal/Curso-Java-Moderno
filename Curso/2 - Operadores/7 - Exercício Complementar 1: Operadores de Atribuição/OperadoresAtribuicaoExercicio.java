public class OperadoresAtribuicaoExercicio {

    public static void main(String[] args) {
        int numero = 5;
        System.out.println(numero);

        numero += 3;
        System.out.println(numero);

        numero -= 2;
        System.out.println(numero);

        numero *= 4;
        System.out.println(numero);

        numero /= 2;
        System.out.println(numero);
    }

}
